#include <stdio.h>
 
// Format Specifications - Practice
int main(void)
{
    // 1
    printf("Year = %d\n", 2026);
 
    // 2
    printf("Your average grade is: %f\n", 95.13);
 
    // 3
    printf("a = %d, b = %d, sum = %d + %d = %d\n",
           3, 5, 3, 5, 3 + 5);
 
    // 4
    printf("We have %.1f coins in the bank\n", 125.7);
 
    // 5
    printf("We have %d coins in the bank\n", 100);
 
    return 0;
}